// index.html pre-built with %BUILD_TIME% substituted.
// malloc'd explicitly so we get a hard failure (not silent String truncation)
// if the heap can't provide the contiguous block we need.
static uint8_t* _htmlBuf = nullptr;
static size_t   _htmlLen = 0;

// Read /index.html from LittleFS into the module-static _htmlBuf.  Called from
// init() with a clean heap AND from startServices() as a fallback if init()
// couldn't run (e.g. LittleFS not yet mounted).  Idempotent — no-op if the
// cache is already built.  Chunked reads via a stack buffer avoid the silent
// truncation that f.readString() suffers on large files.
static void _buildHtmlCache(const char* callerTag) {
    if (_htmlBuf) return;
    File f = LittleFS.open("/index.html", "r");
    if (!f) {
        DEBUG("HTML cache (%s): /index.html not found\n", callerTag);
        return;
    }
    size_t fileSize = f.size();
    _htmlBuf = (uint8_t*)malloc(fileSize + 1);
    if (!_htmlBuf) {
        DEBUG("HTML cache (%s): malloc(%u) failed (heap free: %u, max block: %u)\n",
              callerTag, fileSize + 1, ESP.getFreeHeap(), ESP.getMaxAllocHeap());
        f.close();
        return;
    }
    size_t totalRead = 0;
    uint8_t chunk[512];
    size_t n;
    while ((n = f.read(chunk, sizeof(chunk))) > 0 && totalRead + n <= fileSize) {
        memcpy(_htmlBuf + totalRead, chunk, n);
        totalRead += n;
    }
    _htmlBuf[totalRead] = 0;
    _htmlLen = totalRead;
    DEBUG("HTML cache (%s): %u bytes (heap free: %u)\n",
          callerTag, _htmlLen, ESP.getFreeHeap());
    f.close();
}

static const char *wifi_hostname = "FPVRaceOne";
static const char *wifi_ap_ssid_prefix = "FPVRaceOne";
static const char *wifi_ap_password = "fpvraceone";
static const char *wifi_ap_address = "192.168.4.1";
String wifi_ap_ssid;

// Scan 2.4GHz channels and return the least-congested non-overlapping channel (1, 6, or 11).
// Scoring: each nearby AP adds 1 point plus a RSSI weight (stronger = more interference).
// Lower score = better channel. Called once per boot before starting the AP.
//
// MAC-derived fallback: when the scan sees nothing on any of {1,6,11}, the naive
// "first minimum wins" pick always returns ch 1.  Multiple FPVRaceOne devices
// cold-booting at the same moment in an otherwise-empty RF environment would all
// converge on ch 1.  Hash the device's MAC into {0,1,2} → {ch 1, 6, 11} so
// simultaneous boots deterministically spread across all three channels.  XOR
// of all 6 MAC bytes is fine here: even sequential Espressif MAC allocations
// hash to a uniform distribution mod 3.
static uint8_t selectBestWifiChannel() {
    const uint8_t candidates[] = {1, 6, 11};
    int score[14] = {};  // index 1-13; 0 unused

    WiFi.mode(WIFI_STA);
    int n = WiFi.scanNetworks(false, true, false, 120);  // 120ms per channel max
    if (n > 0) {
        for (int i = 0; i < n; i++) {
            int ch = WiFi.channel(i);
            int rssi = WiFi.RSSI(i);  // negative, e.g. -70
            if (ch >= 1 && ch <= 13) {
                score[ch] += 1 + (rssi + 100) / 20;  // stronger signal = higher score
            }
        }
    }
    WiFi.scanDelete();
    WiFi.mode(WIFI_OFF);

    int bestScore = INT_MAX;
    uint8_t best = candidates[0];
    for (uint8_t ch : candidates) {
        if (score[ch] < bestScore) {
            bestScore = score[ch];
            best = ch;
        }
    }

    if (bestScore == 0) {
        // Scan saw nothing on {1, 6, 11}.  Spread cold-booting nodes across
        // channels deterministically using the device's MAC.
        uint8_t mac[6] = {};
        WiFi.macAddress(mac);
        uint8_t hash = 0;
        for (uint8_t b : mac) hash ^= b;
        best = candidates[hash % 3];
        DEBUG("WiFi channel scan: no APs visible — MAC-fallback ch%d "
              "(mac %02X:%02X:%02X:%02X:%02X:%02X)\n",
              best, mac[0], mac[1], mac[2], mac[3], mac[4], mac[5]);
    } else {
        DEBUG("WiFi channel scan: ch1=%d ch6=%d ch11=%d -> selected ch%d\n",
              score[1], score[6], score[11], best);
    }
    return best;
}

void Webserver::init(Config *config, LapTimer *lapTimer, BatteryMonitor *batMonitor, Buzzer *buzzer, Led *l, RaceHistory *raceHist, Storage *stor, SelfTest *test, RX5808 *rx5808, WebhookManager *webhookMgr, MultiNodeManager *multiNodeMgr) {

    ipAddress.fromString(wifi_ap_address);

    conf = config;
    timer = lapTimer;
    monitor = batMonitor;
    buz = buzzer;
    led = l;
    history = raceHist;
    storage = stor;
    g_storage = stor;  // Set global pointer for static functions
    selftest = test;
    rx = rx5808;
    webhooks = webhookMgr;
    multiNode = multiNodeMgr;
    transportMgr = nullptr;

    // SSID suffix = last 3 bytes of the AP MAC, lowercase, matching the format
    // shown by WiFi scanners.  Two units must always produce different SSIDs.
    //
    // The earlier version used `ESP.getEfuseMac()` and `snprintf(... "%06llX", ...)`.
    // That returned the SAME six chars on every unit because ESP-IDF v5's default
    // nano-printf does not support 64-bit format specifiers (%llX) — it consumes
    // an unrelated stack word and prints constant garbage instead of the masked
    // value.  Read the SoftAP MAC byte-by-byte and print with %02x to avoid the
    // entire problem.  Falls back to all-zero suffix if the read ever fails so
    // we still get a deterministic, non-crashing SSID.
    uint8_t apMac[6] = {0};
    esp_read_mac(apMac, ESP_MAC_WIFI_SOFTAP);
    char macStr[7];
    snprintf(macStr, sizeof(macStr), "%02x%02x%02x", apMac[3], apMac[4], apMac[5]);
    wifi_ap_ssid = String(wifi_ap_ssid_prefix) + "_" + macStr;
    DEBUG("WiFi AP SSID configured: %s (AP MAC %02x:%02x:%02x:%02x:%02x:%02x)\n",
          wifi_ap_ssid.c_str(),
          apMac[0], apMac[1], apMac[2], apMac[3], apMac[4], apMac[5]);

    WiFi.persistent(false);
    // Belt-and-suspenders: also wipe NVS creds + tear down any AP that may
    // have survived a non-clean previous run (crash, brownout, older firmware
    // version without the /reboot cleanup path).
    WiFi.softAPdisconnect(true);
    WiFi.disconnect(true, true);
    WiFi.mode(WIFI_OFF);
    delay(50);

    // ── Diagnostic: log AP station connect / disconnect events ──────────────
    // Registered once at boot.  When a station joins or leaves the master's
    // AP — whether it's a client unit's STA, the director's browser, or a
    // random pilot phone — print the MAC and the resulting station count.
    // This is the only way to tell whether a "master wedged" symptom
    // started with all clients losing their slots simultaneously (suggests
    // an AP / WiFi-stack failure) vs. the browser silently dropping (which
    // doesn't normally take everyone else down).
    WiFi.onEvent([this](WiFiEvent_t event, WiFiEventInfo_t info) {
        if (event == ARDUINO_EVENT_WIFI_AP_STACONNECTED) {
            const uint8_t* mac = info.wifi_ap_staconnected.mac;
            DEBUG("[AP] STA connected: %02x:%02x:%02x:%02x:%02x:%02x  (now %u stations)\n",
                  mac[0], mac[1], mac[2], mac[3], mac[4], mac[5],
                  WiFi.softAPgetStationNum());
        } else if (event == ARDUINO_EVENT_WIFI_AP_STADISCONNECTED) {
            const uint8_t* mac = info.wifi_ap_stadisconnected.mac;
            DEBUG("[AP] STA disconnected: %02x:%02x:%02x:%02x:%02x:%02x  (now %u stations)\n",
                  mac[0], mac[1], mac[2], mac[3], mac[4], mac[5],
                  WiFi.softAPgetStationNum());

            // SSE zombie cleanup.  AsyncEventSource doesn't notice when a
            // client's WiFi peer goes silent — its internal client list
            // still reports count=1 long after the browser disconnects.
            // Every subsequent events.send() queues a message destined for
            // a dead client, slowly bleeding heap (~1 KB/sec observed) and
            // shredding it into fragments small enough that AsyncTCP can't
            // allocate a new PCB — which is the actual root cause of the
            // master-wedges-when-browser-drops bug.
            //
            // We don't know the SSE client's IP from this event, only the
            // station's MAC.  Cross-reference against multiNode's
            // registered client list: if this MAC is one of the 7 race
            // units, leave SSE alone (race units don't open SSE anyway).
            // If it's any other MAC, treat it as a director's browser and
            // close every SSE client.  Worst case a second legitimate
            // viewer's EventSource auto-reconnects within a couple of
            // seconds — vastly preferable to the master crashing.
            char macStr[18];
            snprintf(macStr, sizeof(macStr), "%02X:%02X:%02X:%02X:%02X:%02X",
                     mac[0], mac[1], mac[2], mac[3], mac[4], mac[5]);
            bool isRegisteredClient = false;
            if (multiNode) {
                for (const auto& n : multiNode->getNodes()) {
                    if (n.macAddress.equalsIgnoreCase(macStr)) {
                        isRegisteredClient = true;
                        break;
                    }
                }
            }
            if (!isRegisteredClient && events.count() > 0) {
                DEBUG("[AP] non-client station left — closing %u SSE client(s) to reclaim heap\n",
                      (unsigned)events.count());
                events.close();
            }
        }
    });

    // Determine initial WiFi mode based on node mode.
    //
    // Single + Master always boot AP-only.  Home WiFi credentials (conf.ssid /
    // conf.pwd) are *not* used here even when present — they exist solely so
    // OtaManager can briefly switch to AP+STA during a firmware-update check
    // and then return to AP.  Booting straight into WIFI_STA based on those
    // credentials would silently kill the AP and lock the user out (the device
    // would only be reachable via the home router, which not every user has
    // access to).
    uint8_t nodeMode = conf->getNodeMode();
    if (nodeMode == 2 && conf->getMasterSSID()[0] != 0) {
        // Multi-node client: AP+STA (own AP for pilot phone + STA to master)
        changeMode = WIFI_AP_STA;
    } else {
        changeMode = WIFI_AP;
    }
    // Pre-expire the delay so the first handleWebUpdate() starts WiFi immediately
    // without waiting WIFI_RECONNECT_TIMEOUT_MS on every boot.
    changeTimeMs = millis() - WIFI_RECONNECT_TIMEOUT_MS;
    lastStatus = WL_DISCONNECTED;

    // Build HTML cache HERE, before WiFi starts.  LittleFS is already mounted by
    // storage.init() in main.cpp.  By the time startServices() runs, WiFi+LwIP have
    // consumed ~100 kB of heap and a single 71 kB malloc fails (silently truncating
    // the string).  Building it now gives us a clean heap.
    _buildHtmlCache("init");
}

